    private void action_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_action_btnMouseClicked
        if(action.equals("Add")){
            if(!checkLengths()){
                    
                    return;
                    
            }

            try{
                DBconnector dbc = new DBconnector();
                Connection con = dbc.connect_db();
                String sql = "INSERT INTO tbl_order (customer_id, order_date, total_amount) VALUES (?,?,?)";
                PreparedStatement pst = con.prepareStatement(sql);

                pst.setString(1, custidf_inp.getText());
                pst.setString(2, orderdate_inp.getText());
                pst.setString(3, ordertotal_inp.getText());
                pst.execute();
                JOptionPane.showMessageDialog(null, "Successfully Updated!");
                
                checkLengths();
                close();

            }catch(Exception e){
                System.out.println(e);
            }

        }else if(action.equals("Update")){
                if(!checkLengths()){
                    
                    return;
                    
                }
                DBconnector dbc = new DBconnector();
                dbc.updateData("UPDATE tbl_order SET customer_id = '"+custidf_inp.getText()+"', order_date = '"+orderdate_inp.getText()+"', "
                                         + "total_amount = '"+ordertotal_inp.getText()+"'"+ "WHERE order_id = '"+orderid_inp.getText()+"'");
                close();
        }else{
            JOptionPane.showMessageDialog(null, "No action selected!");
            close();
        }
        
    }//GEN-LAST:event_action_btnMouseClicked

