    private boolean checkLengths() {
    String input = orderdate_inp.getText();
    String regex = "\\d{2}/\\d{2}/\\d{4}";

    if (!input.matches(regex)) {
        JOptionPane.showMessageDialog(null, "Invalid date format: " + input);
        return false;
    }

    return true;
}
    
